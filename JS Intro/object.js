/* Objects
	Objects are variables too. But objects can contain many values.
	The values are written as name: value (name and value separated by a colon)
	A javascript objects is a collection named values.
	The named values in JS objects are called properties.
*/

	let woman1 = {

		name: "Jane Doe",
		age: 35,
		isHappy: true

	}

	console.log(woman1.age);

	let person = {

		name: "Jake Doe",
		age: 30,
		isEmployed: true

	}

	console.log(person.name)
	console.log(person.isEmployed)
	console.log(person)

	/*when creating an object make sure that each property is separated by a comma.*/
	
	
